-- Q1
SELECT AVG(SAL) AS AVG_SAL FROM EMP;

-- Q2
SELECT COUNT(*) AS TOTAL_EMP FROM EMP;

-- Q3
SELECT COUNT(DISTINCT EMPNO) AS DISTINCT_EMP FROM EMP;

-- Q4
SELECT JOB, SUM(SAL) AS TOTAL_SAL
FROM EMP
GROUP BY JOB;

-- Q5
SELECT * FROM EMP
WHERE SAL=(SELECT MAX(SAL) FROM EMP);

-- Q6
SELECT * FROM EMP
WHERE DEPTNO=10 AND SAL=(SELECT MAX(SAL) FROM EMP WHERE DEPTNO=10);

-- Q7
SELECT * FROM EMP
WHERE SAL=((SELECT MAX(SAL) FROM EMP)+(SELECT MIN(SAL) FROM EMP))/2;

-- Q8
SELECT * FROM EMP e1
WHERE EXISTS (
    SELECT 1 FROM EMP e2 
    WHERE e1.HIREDATE=e2.HIREDATE AND e1.EMPNO<>e2.EMPNO
);

